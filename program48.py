#Creating your own Modules----------

def triangle_area(b,h):
    print(f"area of Triangle is : {0.5*b*h}")
def rectangle_area(b,h):
    print(f"area of Rectangle is : {b*h}")
def addiction_area(a,b,c):
    print(f"Addiction area is : {a + b + c}")
def subtriction_area(a,b):
    print(f"area of subtriction : {a - b}")
def multi_area(a,b,c):
    print(f"area of Multi : {a * b * c}")
def divition_area(a,b):
    print(f"area of divition : {a / b}")





