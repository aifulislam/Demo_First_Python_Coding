#For Loop----------------
num = [10,20,30,40,50,60]
print(num)

print('Using While loop----------')
#Using while loop-----------
index = 0
n = len(num)
while index<n:
    print(num[index])
    index = index +1

#Using For Loop-----
print('Using For loop----------')
for x in num:
    print(x)
#All add-----------
print('All add----------')
add = [10,20,30,40,50]
sum = 0
for x in add:
    sum = sum + x
    print(sum)



